PI=3.14
#kvadrat_yuzi
def kvadrat_yuzi(a):
    return a**2

# kvadrat_primetri
def kvadrat_primetri(a):
    return a*4

#uchburchak_yuzi
def uchburchak_yuzi(a,h):
    return (a*h)/2

# uchburchak_primetri
def uchburchak_primetri(a,b,c):
    return a+b+c

#doira yuzi
def doira_yuzi(r):
    global PI
    return PI*(r**2)

#aylana_uzunligi
def aylana_uzunligi(r):
    global PI
    return PI*r*2

